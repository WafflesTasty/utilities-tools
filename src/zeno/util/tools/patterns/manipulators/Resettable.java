package zeno.util.tools.patterns.manipulators;

/**
 * The {@code Resettable} interface defines an object capable of being reset.
 *
 * @author Zeno
 * @since 19 Feb 2020
 * @version 1.0
 */
public interface Resettable
{
	/**
	 * Resets the state of the {@code Resettable}.
	 */
	public abstract void reset();
}